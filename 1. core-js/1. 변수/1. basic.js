

//변수 선언
var total;
console.log(total); //출력 명령

//변수의 초기화 (initialize = 값을 저장해라)
total = 100;
console.log(total);

var result = 10 + 20;
console.log(result);

//변수의 값 변경
total = 70;
console.log(total);

//변수의 값 복사
var copyResult = result;
result = 100;
console.log(copyResult);

total = total * 2;
console.log(total);